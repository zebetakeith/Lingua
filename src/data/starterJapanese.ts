export function isLegacyEnglishStarterDeck(deck: { id?: string; cards?: VocabWord[] }): boolean {
  return deck.id === LEGACY_ENGLISH_STARTER_DECK_ID
    && deck.cards?.[0]?.id === "v001"
    && deck.cards[0].word === "abandon";
}

